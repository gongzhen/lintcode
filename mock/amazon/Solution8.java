// 221. Add Two Numbers II
public class Solution7 {  

    static private class TreeNode {
        int val;
        int count;
        TreeNode left;
        TreeNode right;
        TreeNode(int x) { 
            val = x; 
            count = 1;
            left = null;
            right = null;
        }
    }

    public TreeNode insert(TreeNode head, int val) {
        if(head == null) {
            return new TreeNode(val);
        } else if (val == head.val) {
            head.count++;
        } else if (val < head.val) {
            head.left = insert(head.left, val);
        } else {
            head.count++;
            head.right = insert(head.right, val);
        }
        return head;
    }

    public int search(TreeNode head, long target) {
        if(head == null) {
            return 0;
        } else if(target == head.val) {
            return head.val;
        } else if (target < head.val) {
            return head.count + search(head.left, target);
        } else {
            return search(head.right, target);
        }
    }

    public int reversePairs(int[] nums) {
        TreeNode head = null;
        int n = nums.length;
        int count = 0;
        for(int i = 0; i < n; i++) {
            count += search(head, nums[i] * 2 + 1);
            head = insert(head, nums[i]);
        }
        return count;
    }

    private void printLine() {
    	System.out.println("---------------------"); 
	}

    private void printString(String arg) {
        System.out.println(arg); 
    }    

    private void printArray(int[] list) {
        printStringWithoutNewLine("[");
        int i = 0;
        for(int n : list) {
            printStringWithoutNewLine("[" + i + "]" + n + ", ");
            i++;
        }
        printStringWithoutNewLine("]\n");
    } 

    private void printStringWithoutNewLine(String arg) {
        System.out.print(arg + ","); 
    }

	public static void main(String[] args) {
		Solution7 obj = new Solution7();
        int[] list = new int[]{1,3,2,3,1};        
        System.out.println(obj.reversePairs(list));
	}

}
