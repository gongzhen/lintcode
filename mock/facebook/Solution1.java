class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;
    TreeNode(int x) { val = x; }
}

// 245. Subtree 
public class Solution1 {

    /*
     * @param T1: The roots of binary tree T1.
     * @param T2: The roots of binary tree T2.
     * @return: True if T2 is a subtree of T1, or false.
     */
    public boolean isSubtree(TreeNode T1, TreeNode T2) {
        // write your code here
        if(T2 == null) {
            return true;
        }
        
        if(T1 == null) {
            return false;
        }
        
        
        if(sameTree(T1, T2) == true) {
            return true;
        }
        
        return isSubtree(T1.left, T2) || isSubtree(T1.right, T2);
    }

    private TreeNode createTree() {
         //       5
         //    2      7
         //  1   3      10
         TreeNode node5 = new TreeNode(5);
         TreeNode node2 = new TreeNode(2);
         TreeNode node1 = new TreeNode(1);
         TreeNode node7 = new TreeNode(7);
         TreeNode node10 = new TreeNode(10);

         node5.left = node2;
         node5.right = node7;
         node2.left = node1;
         node5.right = node7;
         node7.right = node10; 
         return node5;
    }    
    
    private boolean sameTree(TreeNode t1, TreeNode t2) {
        if(t1 == null && t2 == null) {
            return true;
        }
        
        if(t1 == null || t2 == null) {
            return false;
        }
        
        if(t1.val != t2.val) {
            return false;
        }
        
        return sameTree(t1.left, t2.left) && sameTree(t1.right, t2.right);
    }

    private int[][] direction = new int[][]{{-1, 0}, {0, 1}, {1, 0}, {0, -1}};

    private void printLine() {
    	System.out.println("---------------------"); 
	}

    private void printString(String arg) {
        System.out.println(arg); 
    }    

    private void printArray(int[] list) {
        printStringWithoutNewLine("[");
        int i = 0;
        for(int n : list) {
            printStringWithoutNewLine("[" + i + "]" + n + ", ");
            i++;
        }
        printStringWithoutNewLine("]\n");
    } 

    private void printStringWithoutNewLine(String arg) {
        System.out.print(arg + ","); 
    } 


	public static void main(String[] args) {
		Solution1 obj = new Solution1();
        TreeNode t1 = obj.createTree();
        TreeNode t2= obj.createTree();
        System.out.println(obj.isSubtree(t1, t2));
	}

}
