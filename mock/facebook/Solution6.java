    /*
     * @param dividend: the dividend
     * @param divisor: the divisor
     * @return: the result
     */
    public int divide(int dividend, int divisor) {
        // write your code here
        if(divisor == 0) {
            return Integer.MIN_VALUE;
        }
        if(dividend == Integer.MIN_VALUE && divisor == -1) {
            return Integer.MAX_VALUE;
        }
        
        int sign = (dividend < 0) ^ (divisor < 0) ? -1 : 1;
        long dd = Math.abs((long)dividend);    
        long dv = Math.abs((long)divisor);
        int res = 0;
        printString("161 dd:" + dd + ",dv:" + dv);
        while(dd >= dv) {
            long temp = dv, multiplier = 1;
            printString("163 res:" + res + ",dd:" + dd);
            while(dd >= temp) {
                printString("165 temp:" + temp + ",multiplier:" + multiplier);
                temp <<= 1;
                multiplier <<= 1;
            }
            printString("168 temp:" + temp + ",multiplier:" + multiplier);
            temp >>= 1;
            multiplier >>= 1;
            printString("171 temp:" + temp + ",multiplier:" + multiplier);
            dd -= temp;
            res += multiplier;
            printString("174 res:" + res + ",dd:" + dd);
        }
        // printString("173 res:" + res + ",dd:" + dd);
        return sign == -1 ? -res : res;

    }
